public class Main {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //declaracion de variables
        
        int num1 = 10;
        int num2 = 5;
        
        
        // 1: suma
        //forma normal
        num1 = num1 + num2;
        System.out.println(num1);
        //forma combinada
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        
        num1 += num2;
        System.out.println(num1);
        
        // 2: resta
        //forma normal
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        num1 = num1 - num2;
        System.out.println(num1);
        //forma combinada
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        
        num1 -= num2;
        System.out.println(num1);
        
        // 3: multiplicacion
        //forma normal
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        num1 = num1 * num2;
        System.out.println(num1);
        //forma combinada
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        
        num1 *= num2;
        System.out.println(num1);
        
        // 4: division
        //forma normal
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        num1 = num1 / num2;
        System.out.println(num1);
        //forma combinada
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        
        num1 /= num2;
        System.out.println(num1);
        
        // 5: modulo
        //forma normal
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        num1 = num1 % num2;
        System.out.println(num1);
        //forma combinada
        num1 = 10; //reasignamos el valor al num1, para que de el mismo resultado
        
        num1 %= num2;
        System.out.println(num1);
       
    }
    
}
